<center><h1>GMAT</h1></center>
<ul>
<li>The Graduate Management Admission Test (GMAT  is a computer adaptive test (CAT) intended to assess certain analytical, writing, quantitative, verbal, and reading skills in written English for use in admission to a graduate management program, such as an MBA program.</li>
<li>It requires knowledge of certain specific grammar and knowledge of certain specific algebra, geometry, and arithmetic. According to the test-owning company, the Graduate Management Admission Council (GMAC), the GMAT assesses analytical writing and problem-solving abilities, while also addressing data sufficiency, logic, and critical reasoning skills that it believes to be vital to real-world business and management success.</li>
<li>The total GMAT score ranges from 200 to 800 and measures performance on the quantitative and verbal sections together (performance on the AWA and IR sections do not count toward the total score, those sections are scored separately). Scores are given in increments of 10 </li>
<li>Age criteria:
The candidate must have completed 18 years of age
There is no upper age limit for the candidate
If the candidate is between 13 to 17 years old, they should have permission in writing from their parents or legal guardian
Educational qualification:
GMAC has not announced any official statement regarding qualification required to appear for GMAT
Candidates who wish to enroll for an MBA programme should possess a graduate degree in any discipline from a recognized university</li>
